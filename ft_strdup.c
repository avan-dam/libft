#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	s;
	char	*ptr;
	int		i;

	s = ft_strlen(s1);
	i = 0;
	ptr = (char *)malloc(sizeof(char) * (s + 1));
	if (ptr == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
